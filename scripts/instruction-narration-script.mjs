/** Вес фразы ≈ время озвучки + пауза в кадре. */
export function estimateCueWeights(texts) {
  return texts.map((text) => 1.2 + text.length * 0.045);
}

export function buildProportionalCues(texts, videoDurationSec, startSec = 0, endPadSec = 2) {
  const weights = estimateCueWeights(texts);
  const total = weights.reduce((a, b) => a + b, 0);
  const usable = Math.max(1, videoDurationSec - startSec - endPadSec);
  let cursor = startSec;
  return texts.map((text, i) => {
    const cue = { startSec: cursor, text };
    cursor += (weights[i] / total) * usable;
    return cue;
  });
}
